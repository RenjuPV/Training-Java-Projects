
public class Main6 {
	public static void main(String[] args) {
		System.out.println("Multiples of 10 between 500 and 1000");
		for (int i = 500; i <= 1000; i += 10) {
			System.out.print(" " + i + "  ");
		}
		System.out.print("\n Multiples of 5 between 2000 and 1000 \n");
		for (int i = 2000; i >= 1000; i -= 5) {
			System.out.print(" " + i + "  ");
		}

	}

}
